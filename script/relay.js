let wordList = ["데이터", "타이쿤", "운명", "명사"];
let lastWord = "데이터";

function startWordRelay() {
    showOverlay("⚡ 끝말잇기 (vs 컴퓨터)");
    
    lastWord = "데이터";
    wordList = ["데이터", "타이쿤", "운명", "명사"];

    const area = document.getElementById('game-area');
    area.innerHTML = `
        <p style="font-size: 20px;">제시어: <strong id="current-word">${lastWord}</strong></p>
        <input type="text" id="r-input" placeholder="단어를 입력하세요">
        <button class="btn-submit" onclick="checkRelay()">확인</button>
        <p id="relay-msg" style="margin-top: 10px;"></p>
    `;
}

function checkRelay() {
    const input = document.getElementById('r-input').value;
    const msg = document.getElementById('relay-msg');

    if (input.length < 2 || lastWord[lastWord.length - 1] !== input[0]) {
        msg.textContent = "틀렸습니다! 다시 입력하세요.";
        msg.style.color = "red";
        return;
    }

    if (wordList.includes(input)) {
        msg.textContent = "이미 사용된 단어입니다!";
        return;
    }

    wordList.push(input);
    lastWord = input;
    document.getElementById('current-word').textContent = lastWord;
    document.getElementById('r-input').value = '';

    setTimeout(computerResponse, 500);
}

function computerResponse() {
    const msg = document.getElementById('relay-msg');
    const nextWord = computerWords.find(w => w[0] === lastWord[lastWord.length - 1] && !wordList.includes(w));

    if (nextWord) {
        lastWord = nextWord;
        wordList.push(nextWord);
        document.getElementById('current-word').textContent = lastWord;
        msg.textContent = `컴퓨터 응답: ${nextWord}`;
        msg.style.color = "blue";
    } else {
        msg.textContent = "컴퓨터가 단어를 찾지 못했습니다! 승리!";
        msg.style.color = "green";
    }
}